let dia = parseInt(prompt("ingrese un numero de la semana: "))
if (dia == 1) {
    console.log("lunes")
}
else if (dia == 2) {
    console.log("martes")
}
else if (dia == 3) {
console.log("miercoles")
}
else if(dia == 4){
    console.log("jueves")
}
else if(dia == 5){
    console.log("viernes")
}
else if(dia ==6){
    console.log("sabado")
}
else if(dia ==7){
    console.log("domingo")
}
else{
    console.log("Error: numero no valido")
}
